package entity;

public class Biblioteca {

    private String nombre;
    private int numeroMaximoLibros;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getNumeroMaximoLibros() {
        return numeroMaximoLibros;
    }

    public void setNumeroMaximoLibros(int numeroMaximoLibros) {
        this.numeroMaximoLibros = numeroMaximoLibros;
    }
}
